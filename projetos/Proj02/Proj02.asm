TEMPO:		;rotina de tempo(2000us)
    MOV R1, #1
LOOP:
    MOV TMOD,#00000001B
    MOV TL0,#LOW(TEMP)
    MOV TH0,#HIGH(TEMP)
    SETB TR0
    JNB TF0,$
    CLR TR0
    CLR TF0
    DJNZ R1,LOOP
    RET
    
TEMPO2:		;rotina de tempo(2000us)
